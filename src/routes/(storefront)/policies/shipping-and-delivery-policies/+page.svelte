<h1>Shipping & Delivery Policies</h1>

When will I get my order?

Usually, it takes 3–7 days to fulfill an order, after which it’s shipped out. The shipping time&nbsp;depends on your location, but can be estimated as follows:

USA: 3–4 business days
Europe: 6–8 business days
Australia: 2–14 business days
Japan: 4–8 business days
International: 10–20 business days



Where will my order ship from?

We work with an on-demand order fulfillment company with facilities worldwide!


Will I be charged customs for my order?

An additional customs and tax fee can occur on international orders. This fee is not in our control&nbsp;and is assessed by your local customs office. Customs policies vary widely for every country so&nbsp;please check with your local customs office directly to see if they apply duties and taxes to your&nbsp;purchases.


My order should be here by now, but I still don't have it. What should I do?

Before getting in touch with us, please help us out by doing the following:

Check your shipping confirmation email for any mistakes in the delivery address
Ask your local post office if they have your package
Stop by your neighbors in case the courier left the package with them

If the shipping address was correct, and the package wasn't left at the post office or at your neighbor’s, <Link href="/contact-us">contact us</Link> with us with your order number.&nbsp;

If you did find a mistake in your delivery address, we can send you a replacement order, but shipping will be at your own cost.



Wrong Address

If you provide an address that is considered insufficient by the courier, the shipment will be returned to our facility. You will be responsible for reshipment costs once we have confirmed an updated address with you (if and as applicable).



Unclaimed Shipments

Shipments that go unclaimed are returned to our facility and you will be responsible for the cost of a reshipment to yourself (if and as applicable).
